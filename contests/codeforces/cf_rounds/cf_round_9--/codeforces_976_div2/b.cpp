void solve() {
    i128 k;
    {
        ll t; cin >> t;
        k = t;
    }
    auto get = [](i128 n) -> i128 {
        i128 sq = bsqrt(n);
        assert((sq*sq) <= n);
        return n-sq;
    };
    i128 low = 1, high = LLONG_MAX;
    while(low < high) {
        i128 mid = (low+high)/i128(2);
        if (get(mid) >= k) high = mid;
        else low = mid+(i128)1ll;
    }
    cout << ll(low) << endl;
}